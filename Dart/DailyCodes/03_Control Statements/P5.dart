//if-else ladder
void main(){
  int x = 1;
  switch(x){
    case 1:
    print("Monday");

    case 2:
    print("Tuesday");

    case 3:
    print("Wednesday");

    case 4:
    print("Thursday");

    default:
    print("Wrong Input");

    case 5:
    print("Friday");

    case 6:
    print("Saturday");

    case 7:
    print("Sunday");

    

  }
}

