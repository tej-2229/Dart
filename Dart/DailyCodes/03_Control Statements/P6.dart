//if-else ladder
void main(){
  double x = 6.5;
  switch(x){
    case 1.5:
    print("Monday");

    case 2.5:
    print("Tuesday");

    case 3.5:
    print("Wednesday");

    case 4.5:
    print("Thursday");

    case 5.5:
    print("Friday");

    case 6.5:
    print("Saturday");

    case 7.5:
    print("Sunday");

    default:
    print("Wrong Input");

  }
}