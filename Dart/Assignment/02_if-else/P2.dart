/*
Write a dart program to print the color of the rainbow based on
the input.
(The color of rainbow are Voilet, Indigo, Blue, Green , Yellow,
Orange Red)
Input : x=1
Output: Violet
Input : x=2

Output: Indigo
*/
void main(){
  var x = 12;
  switch(x){
    case 1:
    print("Voilet");

    case 2:
    print("Indigo");

    case 3:
    print("Blue");

    case 4:
    print("Green");

    case 5:
    print("Yellow");

    case 6:
    print("Orange");

    case 7:
    print("Red");

    default:
    print("Wrong Input!!");
  }
}